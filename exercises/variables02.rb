# hacer un programa que dadas 2 variables llamadas a y b calcule la suma, la resta,
# la multiplicación, la division y el resto y muestre los resultados en la pantalla
# indicando la operación realizada

# primero defino las variables
a = 1579035794730
b = 29485720570

# calculo las operaciones
suma = a + b
